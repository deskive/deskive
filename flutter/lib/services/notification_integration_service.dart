          ];
          break;
        case TaskNotificationType.completed:
          title = 'Task Completed';
          body = '$senderName completed the task: "$taskTitle"';
          actions = [
            NotificationAction(
              id: 'view_task',
              type: NotificationActionType.openItem,
              label: 'View Task',
              data: {'task_id': taskId, 'project_id': projectId},
              isPrimary: true,
            ),
          ];
          break;
        case TaskNotificationType.dueSoon:
          title = 'Task Due Soon';
          body = 'Task "$taskTitle" is due soon';
          priority = NotificationPriority.high;
          actions = [
            NotificationAction(
              id: 'view_task',
              type: NotificationActionType.openItem,
              label: 'View Task',
              data: {'task_id': taskId, 'project_id': projectId},
              isPrimary: true,
            ),
            NotificationAction(
              id: 'snooze',
              type: NotificationActionType.custom,
              label: 'Snooze',
              data: {'task_id': taskId, 'action': 'snooze'},
            ),
          ];
          break;
        case TaskNotificationType.overdue:
          title = 'Task Overdue';
          body = 'Task "$taskTitle" is overdue';
          priority = NotificationPriority.highest;
          actions = [
            NotificationAction(
              id: 'view_task',
              type: NotificationActionType.openItem,
              label: 'View Task',
              data: {'task_id': taskId, 'project_id': projectId},
              isPrimary: true,
            ),
          ];
          break;
        case TaskNotificationType.updated:
          title = 'Task Updated';
          body = '$senderName updated the task: "$taskTitle"';
          actions = [
            NotificationAction(
              id: 'view_task',
              type: NotificationActionType.openItem,
              label: 'View Changes',
              data: {'task_id': taskId, 'project_id': projectId},
              isPrimary: true,
            ),
          ];
          break;
        case TaskNotificationType.commented:
          title = 'New Comment';
          body = '$senderName commented on task: "$taskTitle"';
          actions = [
            NotificationAction(
              id: 'view_task',
              type: NotificationActionType.openItem,
              label: 'View Comment',
              data: {'task_id': taskId, 'project_id': projectId},
              isPrimary: true,
            ),
            NotificationAction(
              id: 'reply',
              type: NotificationActionType.respond,
              label: 'Reply',
              data: {'task_id': taskId, 'action': 'reply'},
            ),
          ];
          break;
      }

      final notification = NotificationModel(
        id: '', // Will be generated by API
        title: title,
        body: body,
        category: NotificationCategory.task,
        priority: priority,
        actions: actions,
        metadata: NotificationMetadata(
          entityId: taskId,
          entityType: 'task',
          workspaceId: workspaceId ?? await AppConfig.getCurrentWorkspaceId(),
          projectId: projectId,
          additionalData: additionalData,
        ),
        createdAt: DateTime.now(),
        senderId: await AppConfig.getCurrentUserId(),
        senderName: senderName,
        senderAvatar: senderAvatar,
      );

      // TODO: Re-implement when backend notification API is ready
      // await _notificationService.apiService.createNotification(notification);
    } catch (e) {
    }
  }

