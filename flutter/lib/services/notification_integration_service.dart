  // Chat notifications
  Future<void> sendChatNotification({
    required String channelId,
    required String channelName,
    required String messageId,
    required String messagePreview,
    required String recipientUserId,
    required String senderName,
    String? senderAvatar,
    required ChatNotificationType type,
    String? workspaceId,
    bool isDirectMessage = false,
    Map<String, dynamic>? additionalData,
  }) async {
    try {
      String title = '';
      String body = '';
      NotificationPriority priority = NotificationPriority.medium;
      List<NotificationAction> actions = [];

      switch (type) {
        case ChatNotificationType.message:
          if (isDirectMessage) {
            title = 'Direct Message';
            body = '$senderName: $messagePreview';
            priority = NotificationPriority.high;
          } else {
            title = 'New Message in #$channelName';
            body = '$senderName: $messagePreview';
          }
          actions = [
            NotificationAction(
              id: 'reply',
              type: NotificationActionType.respond,
              label: 'Reply',
              data: {'channel_id': channelId, 'message_id': messageId, 'action': 'reply'},
              isPrimary: true,
            ),
            NotificationAction(
              id: 'view_channel',
              type: NotificationActionType.openItem,
              label: 'View Channel',
              data: {'channel_id': channelId},
            ),
          ];
          break;
        case ChatNotificationType.mention:
          title = 'You were mentioned';
          body = '$senderName mentioned you in #$channelName: $messagePreview';
          priority = NotificationPriority.high;
          actions = [
            NotificationAction(
              id: 'reply',
              type: NotificationActionType.respond,
              label: 'Reply',
              data: {'channel_id': channelId, 'message_id': messageId, 'action': 'reply'},
              isPrimary: true,
            ),
            NotificationAction(
              id: 'view_message',
              type: NotificationActionType.openItem,
              label: 'View Message',
              data: {'channel_id': channelId, 'message_id': messageId},
            ),
          ];
          break;
        case ChatNotificationType.channelInvite:
          title = 'Channel Invitation';
          body = '$senderName invited you to join #$channelName';
          priority = NotificationPriority.high;
          actions = [
            NotificationAction(
              id: 'accept',
              type: NotificationActionType.approve,
              label: 'Join Channel',
              data: {'channel_id': channelId, 'action': 'join'},
              isPrimary: true,
            ),
            NotificationAction(
              id: 'decline',
              type: NotificationActionType.reject,
              label: 'Decline',
              data: {'channel_id': channelId, 'action': 'decline'},
            ),
          ];
          break;
      }

      final notification = NotificationModel(
        id: '', // Will be generated by API
        title: title,
        body: body,
        category: NotificationCategory.chat,
        priority: priority,
        actions: actions,
        metadata: NotificationMetadata(
          entityId: channelId,
          entityType: 'channel',
          workspaceId: workspaceId ?? await AppConfig.getCurrentWorkspaceId(),
          channelId: channelId,
          additionalData: {
            'message_id': messageId,
            'is_direct_message': isDirectMessage,
            ...?additionalData,
          },
        ),
        createdAt: DateTime.now(),
        senderId: await AppConfig.getCurrentUserId(),
        senderName: senderName,
        senderAvatar: senderAvatar,
      );

      // TODO: Re-implement when backend notification API is ready
      // await _notificationService.apiService.createNotification(notification);
    } catch (e) {
    }
  }

